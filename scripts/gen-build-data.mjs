#!/usr/bin/env node
// gen-build-data.mjs — emit data/build.yaml with site activity stats.
//
// Read by Hugo via hugo.Data.build (footer activity ribbon + /freshness/ page).
// Runs in CI before `hugo build`. data/build.yaml is gitignored.

import { execSync } from 'node:child_process';
import { writeFileSync, mkdirSync } from 'node:fs';

function git(...args) {
  try {
    return execSync(`git ${args.join(' ')}`, { encoding: 'utf8' }).trim();
  } catch {
    return '';
  }
}

const lastCommitISO = git('log', '-1', '--format=%cI');
const lastCommitDays = lastCommitISO
  ? Math.floor((Date.now() - new Date(lastCommitISO).getTime()) / 86_400_000)
  : 0;

const contributors = git('shortlog', '-sn', 'HEAD')
  .split('\n')
  .filter((l) => l.trim().length > 0);
const contributorCount = contributors.length;

const recentUpdatesRaw = git('log', '--since="90 days ago"', '--pretty=format:%h', '--', 'content/', 'data/');
const recentUpdates = recentUpdatesRaw ? recentUpdatesRaw.split('\n').filter(Boolean).length : 0;

const starCount = await (async () => {
  try {
    const [owner, repo] = (process.env.GITHUB_REPOSITORY || '7nolikov/ohmoveagain').split('/');
    const res = await fetch(`https://api.github.com/repos/${owner}/${repo}`, {
      headers: { 'Accept': 'application/vnd.github.v3+json' }
    });
    if (res.ok) return (await res.json()).stargazers_count || 0;
  } catch {}
  return 0;
})();

const out = [
  '# Auto-generated by scripts/gen-build-data.mjs — do not edit.',
  `generatedAt: "${new Date().toISOString()}"`,
  'stats:',
  `  lastCommitDate: "${lastCommitISO}"`,
  `  lastCommitDays: ${lastCommitDays}`,
  `  contributorCount: ${contributorCount}`,
  `  recentUpdates: ${recentUpdates}`,
  `  starCount: ${starCount}`,
  '',
].join('\n');

mkdirSync('data', { recursive: true });
writeFileSync('data/build.yaml', out);
console.log(
  `Wrote data/build.yaml — ${contributorCount} contributors, last commit ${lastCommitDays}d ago, ${recentUpdates} updates in 90d`
);
